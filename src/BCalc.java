public class BCalc {
    
    public static double bcalc_soma(double x, double y){
        double soma = 0;
        soma = Calculadora.soma(x, y);
        return soma;
    }

    public static double bcalc_divi(double x, double y){
        double divi = 0;
        divi = Calculadora.divisao(x, y);
        return divi;
    }

}
